package utilities.enumeration;

/**
 * Enumeration of type of jumps inside sceneries.
 */
public enum Jump {

    /**
     * The jump is caused by a snake.
     */
    SNAKE,

    /**
     * The jump is caused by a ladder.
     */
    LADDER,

    /**
     * If it doesn't need to jump.
     */
    NO_JUMP;

}
